@app.route('/venues')
def venues():
  
  data = []
  places = {}
  
  venuesAll = Venue.query.with_entities(Venue.city, Venue.state, Venue.id, Venue.name).order_by(Venue.state, Venue.city).all()
  for v in venuesAll:
    venue = {
      'id': v.id,
      'name': v.name,
    }
    
    place = v.city + "," + v.state
    
    if place in places:
      places[place].append(venue)
    else:
      places[place] = [venue]
  
  for p in places:
    city, state = p.split(",")
    record = {
      'city': city,
      'state': state,
      'venues': places[p]
    }
    
    data.append(record)
    
  
  return render_template('pages/venues.html', areas=data);

